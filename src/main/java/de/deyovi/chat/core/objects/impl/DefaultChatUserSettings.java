package de.deyovi.chat.core.objects.impl;

import de.deyovi.chat.core.objects.ChatUserSettings;

public class DefaultChatUserSettings implements ChatUserSettings {

	private boolean trusted;
	private boolean asyncmode;
	private String color = "000000";
	private String room;
	private String font;

	@Override
	public String getColor() {
		return color;
	}

	@Override
	public void setColor(String color) {
		this.color = color;
	}

	@Override
	public void setFont(String font) {
		this.font = font;
	}

	@Override
	public String getFont() {
		return font;
	}

	@Override
	public void setFavouriteRoom(String room) {
		this.room = room;
	}

	@Override
	public String getFavouriteRoom() {
		return room;
	}

	@Override
	public void setTrusted(boolean trusted) {
		this.trusted = trusted;
	}
	
	@Override
	public boolean isTrusted() {
		return trusted;
	}
	
	@Override
	public boolean hasAsyncMode() {
		return asyncmode;
	}
	
	@Override
	public void setAsyncmode(boolean asyncmode) {
		this.asyncmode = asyncmode;
	}
	
}
